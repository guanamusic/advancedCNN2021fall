import torch
import numpy as np


def torch_nearest_upsampler(x, factor):
    """
    Due to torch.nn.functional.interpolate is too slow, use this method.
    """
    assert len(x.size()) == 4
    assert int(factor) == factor, f"Upsampling factor has to integer, not {factor}."

    # temp assertion line
    assert factor == 2 or factor == 8, f"factor=={factor}."

    return x.view(x.size(0), x.size(1), -1, 1).repeat(1, 1, 1, factor)\
        .view(x.size(0), x.size(1), x.size(2), -1).repeat(1, 1, 1, factor)\
        .view(x.size(0), x.size(1), x.size(2) * factor, x.size(3) * factor)


def torch_nearest_downsampler(x, factor):
    """
    Due to torch.nn.functional.interpolate is too slow, use this method.
    """
    assert len(x.size()) == 4
    assert int(factor) == factor, f"Downsampling factor has to integer, not {factor}."

    # temp assertion line
    assert factor == 2 or factor == 8, f"factor=={factor}."

    return x[:, :, :(x.size(2)//factor)*factor, :(x.size(3)//factor)*factor][:, :, ::factor, ::factor]


def crop_only_masked_region(x, mask):
    """
    Semantically identical with local_patch of daa233's repo.
    """
    assert len(x.size()) == 4
    # assume that all batches
    mask_0th = mask[0]
    _, dim_2, dim_3 = (mask_0th == 1).nonzero(as_tuple=True)
    return x[:, :, dim_2.min():(dim_2.max()+1), dim_3.min():(dim_3.max()+1)]


